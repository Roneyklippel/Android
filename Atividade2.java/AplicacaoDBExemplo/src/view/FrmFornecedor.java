        //testando a conexão
        ConnectionFactory conexao = new ConnectionFactory();
        if (conexao.getConnection()) {
            try {
                String sql = "SELECT * FROM fornecedor";
                PreparedStatement stmt = conexao.connection.prepareStatement(sql);
                ResultSet rs = stmt.executeQuery();
                //imprimindo os titulos da tabela
                System.out.println("CÓDIGO NOME TELEFONE");
                System.out.println("----------------------");
                //Imprimindo os dados do ResultSet
                while(rs.next()){
                    System.out.println(rs.getInt("codigo")+" "+rs.getString("nome")+" "+rs.getString("telefone"));
                }
                rs.close();
                stmt.close();
                conexao.close();
            } catch (SQLException e) {
                System.out.println("ERRO: "+e);
            }
            
        } else {
        }
